    private void jButtonSearchRecordsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchRecordsActionPerformed
        DefaultTableModel stockRecordsTable = (DefaultTableModel) jTableStockRecords.getModel();

        String attribute = "";
        int selectedIndex = jComboBoxAttribute.getSelectedIndex();
        switch (selectedIndex) {
            case 0:
                attribute = "prod_id";
                break;
            case 1:
                attribute = "prod_name";
                break;
            case 2:
                attribute = "prod_category";
                break;
            case 3:
                attribute = "price";
                break;
            case 4:
                attribute = "ideal_stock";
                break;
            case 5:
                attribute = "avail_stock";
                break;
            default:
                break;
        }

        String value = jTextFieldValue.getText();
        if (!value.trim().isEmpty()) {
            int tableRows = stockRecordsTable.getRowCount();
            if (tableRows > 0) {
                for (int i = 0; i < tableRows; i++) {
                    stockRecordsTable.removeRow(0);
                }
            }

            try {
                Class.forName("java.sql.DriverManager");
                Connection conn = (Connection) DriverManager.getConnection(
                        "jdbc:mysql://127.0.0.1/cafe_management_system",
                        userName,
                        password
                );
                Statement stmt = (Statement) conn.createStatement();
                String query = "SELECT prod_id, prod_name, prod_category, price, ideal_stock, avail_stock "
                        + "FROM stocks WHERE " + attribute + " LIKE '%" + value + "%';";
                ResultSet resultSet = stmt.executeQuery(query);
                if (resultSet.next()) {
                    do {
                        int id = resultSet.getInt("prod_id");
                        String prodName = resultSet.getString("prod_name");
                        String prodCat = resultSet.getString("prod_category");
                        int price = resultSet.getInt("price");
                        int idealStock = resultSet.getInt("ideal_stock");
                        int availStock = resultSet.getInt("avail_stock");
                        stockRecordsTable.addRow(new Object[]{
                            id, prodName, prodCat, price, idealStock, availStock
                        });
                    } while (resultSet.next());
                } else {
                    JOptionPane.showMessageDialog(this, "No Customer Record Found...");
                }
            } catch (ClassNotFoundException | SQLException exce) {
                JOptionPane.showMessageDialog(this, exce.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Value Text Field Cannot Be Empty...");
        }

    }//GEN-LAST:event_jButtonSearchRecordsActionPerformed

