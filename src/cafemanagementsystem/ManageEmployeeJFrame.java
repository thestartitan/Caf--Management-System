    private void jButtonSearchRecordsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchRecordsActionPerformed
        DefaultTableModel employeeRecordsTable = (DefaultTableModel) jTableEmployeeRecords.getModel();

        String attribute = "";
        int selectedIndex = jComboBoxAttribute.getSelectedIndex();
        switch (selectedIndex) {
            case 0:
                attribute = "emp_id";
                break;
            case 1:
                attribute = "emp_name";
                break;
            case 2:
                attribute = "phone_no";
                break;
            case 3:
                attribute = "emp_title";
                break;
            case 4:
                attribute = "gender";
                break;
            case 5:
                attribute = "emp_doj";
                break;
            case 6:
                attribute = "email_addr";
                break;
            case 7:
                attribute = "address";
                break;
            default:
                break;
        }

        String value = jTextFieldValue.getText();
        if (!value.trim().isEmpty()) {
            int tableRows = employeeRecordsTable.getRowCount();
            if (tableRows > 0) {
                for (int i = 0; i < tableRows; i++) {
                    employeeRecordsTable.removeRow(0);
                }
            }

            try {
                Class.forName("java.sql.DriverManager");
                Connection conn = (Connection) DriverManager.getConnection(
                        "jdbc:mysql://127.0.0.1/cafe_management_system",
                        userName,
                        password
                );
                Statement stmt = (Statement) conn.createStatement();

                String query;
                if (attribute.equals("gender")) {
                    if (value.equalsIgnoreCase("M")) {
                        value = "Male";
                    } else if (value.equalsIgnoreCase("F")) {
                        value = "Female";
                    }
                    query = "SELECT emp_id, emp_name, phone_no, emp_title, gender, emp_doj, email_addr, address FROM employees "
                            + "WHERE gender = '" + value + "';";
                } else {
                    query = "SELECT emp_id, emp_name, phone_no, emp_title, gender, emp_doj, email_addr, address FROM employees "
                            + "WHERE " + attribute + " LIKE '%" + value + "%';";
                }

                ResultSet resultSet = stmt.executeQuery(query);
                if (resultSet.next()) {
                    do {
                        int id = resultSet.getInt("emp_id");
                        String empName = resultSet.getString("emp_name");
                        String phoneNo = resultSet.getString("phone_no");
                        String gender = resultSet.getString("gender");
                        String empTitle = resultSet.getString("emp_title");
                        String dateOfJoining = resultSet.getString("emp_doj");
                        String emailAddr = resultSet.getString("email_addr");
                        String residence = resultSet.getString("address");
                        employeeRecordsTable.addRow(new Object[]{
                            id, empName, phoneNo, empTitle, gender, dateOfJoining, emailAddr, residence
                        });
                    } while (resultSet.next());
                } else {
                    JOptionPane.showMessageDialog(this, "No Employee Record Found...");
                }
            } catch (ClassNotFoundException | SQLException exce) {
                JOptionPane.showMessageDialog(this, exce.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Value Text Field Cannot Be Empty...");
        }

    }//GEN-LAST:event_jButtonSearchRecordsActionPerformed

