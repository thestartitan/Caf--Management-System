    public void refreshRecords() {
        DefaultTableModel employeeRecordsTable = (DefaultTableModel) jTableEmployeeRecords.getModel();

        int tableRows = employeeRecordsTable.getRowCount();
        if (tableRows > 0) {
            for (int i = 0; i < tableRows; i++) {
                employeeRecordsTable.removeRow(0);
            }
        }

        try {
            Class.forName("java.sql.DriverManager");
            Connection conn = (Connection) DriverManager.getConnection(
                    "jdbc:mysql://127.0.0.1/cafe_management_system",
                    userName,
                    password
            );
            Statement stmt = (Statement) conn.createStatement();
            String query = "SELECT emp_id, emp_name, phone_no, emp_title, gender, emp_doj, email_addr, address FROM employees;";
            ResultSet resultSet = stmt.executeQuery(query);
            if (resultSet.next()) {
                do {
                    int id = resultSet.getInt("emp_id");
                    String empName = resultSet.getString("emp_name");
                    String phoneNo = resultSet.getString("phone_no");
                    String gender = resultSet.getString("gender");
                    String empTitle = resultSet.getString("emp_title");
                    String dateOfJoining = resultSet.getString("emp_doj");
                    String emailAddr = resultSet.getString("email_addr");
                    String residence = resultSet.getString("address");
                    employeeRecordsTable.addRow(new Object[]{
                        id, empName, phoneNo, empTitle, gender, dateOfJoining, emailAddr, residence
                    });
                } while (resultSet.next());
            } else {
                JOptionPane.showMessageDialog(null, "No Employee Record to Load...");
            }
        } catch (ClassNotFoundException | SQLException exce) {
            JOptionPane.showMessageDialog(null, exce.getMessage());
        }
    }

